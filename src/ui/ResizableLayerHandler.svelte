<script lang="ts">
  import Layer from './Layer.svelte';
  import { COLORS, type RenderProps } from '../lib';

  export let x: number;
  export let y: number;
  export let active: boolean;

  $: render = ({ ctx }: RenderProps) => {
    if (!ctx) return;
    ctx.fillStyle = active ? COLORS.FLIPPED_CARD_FRONT_BACKGROUND : COLORS.SELECTION;
    ctx.fillRect(x - 6, y - 6, 12, 12);
  };
</script>

<Layer {render} on:mouseenter on:mouseleave on:mousedown on:mouseup on:touchstart on:touchend />
